def make_grid(s):
    return [list(line) for line in s.splitlines() if line]


def get_neighbors(grid, x, y):
    above = grid[y - 1] if y > 0 else []
    below = grid[y + 1] if y < len(grid) - 1 else []
    cur   = grid[y]
    left, right = max(x - 1, 0), min(x + 2, len(cur))

    above = above[left : right]
    cur_left = cur[left : x]
    cur_right = cur[x + 1 : right]
    below = below[left : right]

    return above + cur_left + cur_right + below

test_neighbors = """
AxB...
CDE...
......
ab123.
zc4y5.
de678.
"""

assert get_neighbors(make_grid(test_neighbors), 1, 0) == list('ABCDE')
assert get_neighbors(make_grid(test_neighbors), 3, 4) == list('12345678')
assert get_neighbors(make_grid(test_neighbors), 0, 4) == list('abcde')


def advance_grid(cur_grid):
    next_grid = []
    for y, row in enumerate(cur_grid):
        next_row = []
        for x, el in enumerate(row):
            neighs = get_neighbors(cur_grid, x, y)
            num_on_neighs = sum(1 for el in neighs if el)
            if el:
                next_el = num_on_neighs in [2, 3]
            else:
                next_el = num_on_neighs == 3
            next_row.append(next_el)
        next_grid.append(next_row)
    return next_grid


def print_grid(grid):
    for row in grid:
        print(''.join(['#' if el else '.' for el in row]))


grid = make_grid(inp)
grid = [[el == '#' for el in row] for row in grid]


for i in range(inp_steps):
    grid = advance_grid(grid)

print(sum(sum(1 for el in row if el) for row in grid))


